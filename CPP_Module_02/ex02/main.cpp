#include "Fixed.hpp"

int main( void )
{
	Fixed a;
	Fixed const b( Fixed( 500000.05f ) * Fixed( 200000.0f ) );
	
	std::cout << a << std::endl;
	std::cout << ++a << std::endl;
	std::cout << a << std::endl;
	std::cout << a++ << std::endl;
	std::cout << a << std::endl;
	
	std::cout << b << std::endl;

	std::cout << Fixed::max( a, b ) << std::endl;

	return 0;
}